package com.codetrio.module.codegroup;

public class CodeGroupDto {
	
	private String cgSeq;
	private String cgName;
	private String cgRegDate;
	private String cgMofDate;
	private int cCount;

	public String getCgSeq() {
		return cgSeq;
	}

	public void setCgSeq(String cgSeq) {
		this.cgSeq = cgSeq;
	}

	public String getCgName() {
		return cgName;
	}
	
	public void setCgName(String cgName) {
		this.cgName = cgName;
	}
	
	
	public String getCgRegDate() {
		return cgRegDate;
	}

	public void setCgRegDate(String cgRegDate) {
		this.cgRegDate = cgRegDate;
	}

	public String getCgMofDate() {
		return cgMofDate;
	}

	public void setCgMofDate(String cgMofDate) {
		this.cgMofDate = cgMofDate;
	}

	public int getcCount() {
		return cCount;
	}

	public void setcCount(int cCount) {
		this.cCount = cCount;
	}
	
}
